#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (*s != '\0')
	{
		s++;
		i++;
	}
	while (*s != (char)c && i >= 0)
	{
		i--;
		s--;
	}
	if (*s == (char)c)
		return ((char *)s);
	return (NULL);
}
