#include <string.h>

char	*ft_strcat(char *s1, const char *s2)
{
	int	s1_i;
	int	s2_i;

	s1_i = 0;
	s2_i = 0;
	while (s1[s1_i] != '\0')
		s1_i++;
	while (s2[s2_i] != '\0')
	{
		s1[s1_i + s2_i] = s2[s2_i];
		s2_i++;
	}
	s1[s1_i + s2_i] = '\0';
	return (s1);
}
