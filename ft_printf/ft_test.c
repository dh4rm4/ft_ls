#include "libftprintf.h"

int	ft_test(char c)
{
	if (c != '.' && c != 's' && c != 'S' && c != 'p' && c != 'd' &&
		c != 'D' && c != 'i' && c != 'o' && c != 'O' && c != 'u' &&
		c != 'U' && c != 'x' && c != 'X' && c != 'c' && c != 'C')
		return (1);
	return (0);
}
